                <br>
                <br>
                In 1614, Cavite was created as a politico-military province.
                <br>
                <br>
                In 1872, a mutiny at the Cavite arsenal implicated three Filipino priests: Jose Burgos, 
                Mariano Gomez and Jacinto Zamora, who were subsequently executed by the garrote.
                <br>
                <br>
                Cavite was a center of military operations during the Philippine Revolution of 1896. 
                The Spaniards executed 13 Filipino revolutionaries in Cavite City that year: the provincial 
                capital is now named after them "Trece Martires City".
                <br>
                <br>
                On June 12, 1898, General Emilio Aguinaldo proclaimed Philippine independence from his residence in Kawit. 
                Bacoor became the capital until the seat of government was transferred to Malolos, Bulacan.
                A civil government was established by the Americans in Cavite in 1901, with Cavite City as provincial capital.
                <br>
                <br>
                Trece Martires City became the capital upon its creation in 1954.
